        try {

            int subId = jComboBox1.getSelectedIndex();

            ResultSet resultSet = MySQL.excute("SELECT * FROM `teacher` INNER JOIN `subject` ON `subject`.`Subno`=`teacher`.`subjects`INNER JOIN `geder` ON `geder`.`Id`=`teacher`.`gender_id`  WHERE `subjects`='" + subId + "' ORDER BY `Tno` ASC");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();
                v.add(resultSet.getString("Tno"));
                v.add(resultSet.getString("Name"));
                v.add(resultSet.getString("Address"));
                v.add(resultSet.getString("Description"));
                v.add(resultSet.getString("gender"));
                v.add(resultSet.getString("contact_no"));

                model.addRow(v);
                jTable1.setModel(model);
            }

            if (subId == 0) {
                loadUsers();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
