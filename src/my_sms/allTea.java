    private void loadSubjects() {
        try {

            ResultSet resultSet = MySQL.excute("SELECT * FROM `subject`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("Description"));
                subjectymap.put(resultSet.getString("Description"), resultSet.getInt("Subno"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);

        } catch (Exception e) {

            e.printStackTrace();
        }
    }

    private void loadUsers() {
        try {

            ResultSet resultSet = MySQL.excute("SELECT * FROM `teacher` INNER JOIN `subject` ON `subject`.`Subno`=`teacher`.`subjects`INNER JOIN `geder` ON `geder`.`Id`=`teacher`.`gender_id` ORDER BY `Tno` ASC\n"
                    + "");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> v = new Vector();
                v.add(resultSet.getString("Tno"));
                v.add(resultSet.getString("Name"));
                v.add(resultSet.getString("Address"));
                v.add(resultSet.getString("Description"));
                v.add(resultSet.getString("gender"));
                v.add(resultSet.getString("contact_no"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

